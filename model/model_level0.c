int classify_level0(const float fv[6]) {
    float x[6];
    for (int i = 0; i < 6; i++)
        x[i] = (fv[i] - SCALER_MEAN[i]) / SCALER_SCALE[i];

    // Hidden layer (6 -> 16) with ReLU
    float h[16];
    for (int j = 0; j < 16; j++) {
        float sum = MLP_B0[j];
        for (int i = 0; i < 6; i++)
            sum += x[i] * MLP_W0[i][j];
        h[j] = sum > 0.0f ? sum : 0.0f;  // ReLU
    }

    // Output layer (16 -> 3)
    float scores[3];
    for (int c = 0; c < 3; c++) {
        scores[c] = MLP_B1[c];
        for (int j = 0; j < 16; j++)
            scores[c] += h[j] * MLP_W1[j][c];
    }

    // Argmax
    int best = 0;
    for (int c = 1; c < 3; c++)
        if (scores[c] > scores[best]) best = c;

    return best;  // 0=tone, 1=noise, 2=speech
}
